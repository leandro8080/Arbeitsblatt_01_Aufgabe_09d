function multiplyNumbers(a, b) {
	return a * b;
}

console.log(multiplyNumbers(1, 2));
console.log(multiplyNumbers(2, 5));
console.log(multiplyNumbers(100, 220));
console.log(multiplyNumbers(110, 2391));
console.log(multiplyNumbers(0.2, 1));

function getFahrenheit(celsius) {
	return celsius * 1.8 + 32;
}

console.log(getFahrenheit(25));
console.log(getFahrenheit(0));

const getModule = (a, b) => {
	return a % b;
};

console.log(getModule(2, 29));
console.log(getModule(3, 29));
